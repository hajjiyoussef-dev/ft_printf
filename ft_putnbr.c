#include "libftprintf.h"

void ft_putnbr(int nbr, int *len)
{
    if (nbr == -2147483648)
    {
        ft_putstr("-2147483648", len);
        return ;
    }
    if (nbr < 0)
    {
        ft_putchar('-', len);
        nbr = -nbr;
    }
}